import math
def funciones_math():
    print(round(9.9)) #Redondeo tanto por exceso como por defecto
    print(round(1,3))
    print(abs(99.8)) #Valor absoluto positivo
    print(abs(-99.8))
    print(math.ceil(89.4)) #El entero superior más cercano
    print(math.floor(89.4)) #El entero inferior más cercano
    print(math.pow(8,4)) #Elevar x a y lo
    print(math.sqrt(9)) #Raiz cuadrada de x
    print(math.pi) #Valor de Pi= 3.14
    print(math.factorial(9)) # Valor de factorial de x
    print(math.isfinite(9.8)) #X es una valor finito o infinito
    print(math.isinf(9)) # X es una valor infinito tanto positivo como negativo
    print(math.isnan(10)) # X es una valor natural o no
